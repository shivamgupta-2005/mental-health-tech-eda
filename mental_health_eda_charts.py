plt.xlabel('Family History')
plt.ylabel('Number of Respondents')
plt.legend(title='Sought Treatment')
plt.tight_layout()
plt.show()

"""## ðŸ“Š Chart 3: Remote Work vs Work Interference"""

plt.figure(figsize=(8,5))
